/** Fallback chain: exact locale → en. */
export function getSpinnerVerbs(locale: string): string[] {
  return SPINNER_VERBS_BY_LOCALE[locale] || SPINNER_VERBS_BY_LOCALE.en;
}

/** Wrap a verb in the locale-appropriate "doing X right now" phrase. */
export function formatSpinnerVerb(verb: string, locale: string): string {
  if (locale === 'ja') return `${verb}中…`;
  if (locale.startsWith('zh')) return `正在${verb}中…`;
  return `${verb}…`;
}
